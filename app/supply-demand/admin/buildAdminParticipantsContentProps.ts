import type { AdminPageContentProps } from "@/app/supply-demand/admin/AdminPageContent";
import type { AdminPageContentBuilderContext } from "@/app/supply-demand/admin/AdminPageContentBuilderContext";

export function buildAdminParticipantsContentProps({
  actions,
  derived,
  drafts,
  queries,
}: AdminPageContentBuilderContext): NonNullable<AdminPageContentProps["participantsProps"]> {
  const {
    autoParticipantEditDraft,
    autoParticipantEditDraftSetters,
    autoParticipantUserKey,
    clearAutoStrategyDraft,
    editingAutoParticipantUserKey,
    editingStrategyKey,
    resetAutoParticipantDraft,
    selectAutoStrategyDraft,
    setAutoParticipantUserKey,
    setStrategyEnabled,
    setStrategyIntensity,
    strategyEnabled,
    strategyIntensity,
    strategySymbol,
    strategyUserKey,
  } = drafts.autoParticipant;
  const {
    draft: autoParticipantGenerateDraft,
    setCount: setAutoGenerateCount,
    setCreateAccount: setAutoGenerateCreateAccount,
    setDisplayPrefix: setAutoGenerateDisplayPrefix,
    setInitialCashAmount: setAutoGenerateInitialCashAmount,
    setKeyPrefix: setAutoGenerateKeyPrefix,
    setProfileMode: setAutoGenerateProfileMode,
    setProfileType: setAutoGenerateProfileType,
    setRecurringCashAmount: setAutoGenerateRecurringCashAmount,
    setRecurringCashIntervalUnit: setAutoGenerateRecurringCashIntervalUnit,
    setRecurringCashIntervalValue: setAutoGenerateRecurringCashIntervalValue,
  } = drafts.autoParticipantGenerate;
  const {
    participantList,
    selectAutoParticipantDraft,
    selectParticipantStrategySymbolDraft,
    selectedAutoParticipant,
    selectedAutoParticipantSymbolConfigs,
    selectedParticipantStrategyKey,
  } = derived;
  const { status } = queries;
  const {
    adjustingCashType,
    adjustAutoParticipantCashBalance,
    generateAutoParticipants,
    generatingAutoParticipants,
    savingAutoParticipant,
    savingStrategy,
    submitAutoParticipant,
    submitAutoStrategy,
    toggleAutoParticipantEnabled,
    toggleAutoStrategyEnabled,
    togglingAutoParticipantUserKey,
    togglingStrategyKey,
    withdrawingAutoParticipantUserKey,
    withdrawAutoParticipantRow,
  } = actions;

  return {
    adjustingCashType,
    autoGenerateCount: autoParticipantGenerateDraft.count,
    autoGenerateCreateAccount: autoParticipantGenerateDraft.createAccount,
    autoGenerateDisplayPrefix: autoParticipantGenerateDraft.displayPrefix,
    autoGenerateInitialCashAmount: autoParticipantGenerateDraft.initialCashAmount,
    autoGenerateKeyPrefix: autoParticipantGenerateDraft.keyPrefix,
    autoGenerateProfileMode: autoParticipantGenerateDraft.profileMode,
    autoGenerateProfileType: autoParticipantGenerateDraft.profileType,
    autoGenerateRecurringCashAmount: autoParticipantGenerateDraft.recurringCashAmount,
    autoGenerateRecurringCashIntervalUnit: autoParticipantGenerateDraft.recurringCashIntervalUnit,
    autoGenerateRecurringCashIntervalValue: autoParticipantGenerateDraft.recurringCashIntervalValue,
    draft: autoParticipantEditDraft,
    draftSetters: autoParticipantEditDraftSetters,
    editingStrategyKey,
    editingUserKey: editingAutoParticipantUserKey,
    generatingAutoParticipants,
    isEditing: editingAutoParticipantUserKey !== null,
    onAdjustCash: adjustAutoParticipantCashBalance,
    onAutoGenerateCountChange: setAutoGenerateCount,
    onAutoGenerateCreateAccountChange: setAutoGenerateCreateAccount,
    onAutoGenerateDisplayPrefixChange: setAutoGenerateDisplayPrefix,
    onAutoGenerateInitialCashAmountChange: setAutoGenerateInitialCashAmount,
    onAutoGenerateKeyPrefixChange: setAutoGenerateKeyPrefix,
    onAutoGenerateProfileModeChange: setAutoGenerateProfileMode,
    onAutoGenerateProfileTypeChange: setAutoGenerateProfileType,
    onAutoGenerateRecurringCashAmountChange: setAutoGenerateRecurringCashAmount,
    onAutoGenerateRecurringCashIntervalUnitChange: setAutoGenerateRecurringCashIntervalUnit,
    onAutoGenerateRecurringCashIntervalValueChange: setAutoGenerateRecurringCashIntervalValue,
    onClearStrategy: clearAutoStrategyDraft,
    onGenerateParticipants: generateAutoParticipants,
    onParticipantUserKeyChange: setAutoParticipantUserKey,
    onResetParticipantDraft: resetAutoParticipantDraft,
    onSelectParticipant: selectAutoParticipantDraft,
    onSelectStrategyDraft: selectAutoStrategyDraft,
    onSelectStrategySymbol: selectParticipantStrategySymbolDraft,
    onStrategyEnabledChange: setStrategyEnabled,
    onStrategyIntensityChange: setStrategyIntensity,
    onSubmitParticipant: submitAutoParticipant,
    onSubmitStrategy: submitAutoStrategy,
    onToggleParticipant: toggleAutoParticipantEnabled,
    onToggleStrategyEnabled: toggleAutoStrategyEnabled,
    onWithdrawParticipant: withdrawAutoParticipantRow,
    participantList,
    participantUserKey: autoParticipantUserKey,
    saving: savingAutoParticipant,
    savingStrategy,
    selectedParticipant: selectedAutoParticipant,
    selectedStrategyKey: selectedParticipantStrategyKey,
    selectedSymbolConfigs: selectedAutoParticipantSymbolConfigs,
    strategyEnabled,
    strategyIntensity,
    strategySymbol,
    strategySymbols: (status?.configs ?? []).map((config) => config.symbol),
    strategyUserKey,
    togglingStrategyKey,
    togglingUserKey: togglingAutoParticipantUserKey,
    withdrawingUserKey: withdrawingAutoParticipantUserKey,
  };
}
